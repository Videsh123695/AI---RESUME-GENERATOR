const pdfParse = require("pdf-parse");

const {
  generateInterviewReport,
  generateResumePdf,
} = require("../services/ai.service");

const interviewReportModel = require("../models/interviewReport.model");

/*
|--------------------------------------------------------------------------
| Generate Interview Report Controller
|--------------------------------------------------------------------------
|
| Flow:
| 1. Receive resume PDF + user inputs
| 2. Extract text from PDF
| 3. Send extracted data to AI service
| 4. Save generated report in MongoDB
|
*/

async function generateInterviewReportController(req, res) {
  try {
    // Check resume file exists
    if (!req.file) {
      return res.status(400).json({
        message: "Please upload resume file",
      });
    }

    const { selfDescription, jobDescription } = req.body;

    // Validate required fields
    if (!selfDescription || !jobDescription) {
      return res.status(400).json({
        message: "Self description and job description are required",
      });
    }

    // Extract text from uploaded PDF
    const resumeContent = await new pdfParse.PDFParse(
      Uint8Array.from(req.file.buffer),
    ).getText();

    // Generate report using AI
    const aiReport = await generateInterviewReport({
      resume: resumeContent.text,

      selfDescription,

      jobDescription,
    });

    // Save report in database
    const interviewReport = await interviewReportModel.create({
      user: req.user.id,

      resume: resumeContent.text,

      selfDescription,

      jobDescription,

      // AI generated fields
      ...aiReport,
    });

    return res.status(201).json({
      message: "Interview report generated successfully",

      interviewReport,
    });
  } catch (error) {
    console.error("Generate Interview Report Error:", error.message);

    return res.status(500).json({
      message: "Failed to generate interview report",
    });
  }
}

/*
|--------------------------------------------------------------------------
| Get Single Interview Report Controller
|--------------------------------------------------------------------------
|
| Fetch report by id
| Only owner user can access the report
|
*/

async function generateInterviewReportByIdController(req, res) {
  try {
    const { interviewId } = req.params;

    const interviewReport = await interviewReportModel.findOne({
      _id: interviewId,

      user: req.user.id,
    });

    if (!interviewReport) {
      return res.status(404).json({
        message: "Interview report not found",
      });
    }

    return res.status(200).json({
      message: "Interview report fetched successfully",

      interviewReport,
    });
  } catch (error) {
    console.error("Fetch Report Error:", error.message);

    if (error.name === "CastError") {
      return res.status(400).json({
        message: "Invalid interview id",
      });
    }

    return res.status(500).json({
      message: "Failed to fetch interview report",
    });
  }
}

/*
|--------------------------------------------------------------------------
| Get All Interview Reports Controller
|--------------------------------------------------------------------------
|
| Fetch all reports generated by logged-in user
| Heavy fields are removed for dashboard listing
|
*/

async function getAllInterviewReportsController(req, res) {
  try {
    const interviewReports = await interviewReportModel

      .find({
        user: req.user.id,
      })

      .sort({
        createdAt: -1,
      })

      .select(
        `
          -resume
          -selfDescription
          -jobDescription
          -technicalQuestion
          -behavioralQuestion
          -skillGaps
          -preperationPlan
          -__v
          `,
      );

    return res.status(200).json({
      message: "Interview reports fetched successfully",

      interviewReports,
    });
  } catch (error) {
    console.error("Fetch Reports Error:", error.message);

    return res.status(500).json({
      message: "Failed to fetch reports",
    });
  }
}

/*
|--------------------------------------------------------------------------
| Generate Optimized Resume PDF Controller
|--------------------------------------------------------------------------
|
| Flow:
| 1. Get stored resume + JD
| 2. AI rewrites resume
| 3. Convert HTML to PDF
| 4. Send downloadable PDF
|
*/

async function generateResumePdfController(req, res) {
  try {
    const { interviewReportId } = req.params;

    // Find only user's own report
    const interviewReport = await interviewReportModel.findOne({
      _id: interviewReportId,

      user: req.user.id,
    });

    if (!interviewReport) {
      return res.status(404).json({
        message: "Interview report not found",
      });
    }

    const {
      resume,

      jobDescription,

      selfDescription,
    } = interviewReport;

    // Generate optimized resume PDF buffer
    const pdfBuffer = await generateResumePdf({
      resume,

      jobDescription,

      selfDescription,
    });

    // Send PDF file response
    res.set({
      "Content-Type": "application/pdf",

      "Content-Disposition": `attachment; filename=resume_${interviewReportId}.pdf`,
    });

    return res.send(pdfBuffer);
  } catch (error) {
    console.error(
      "Resume PDF Error:",

      error.message,
    );

    return res.status(500).json({
      message: "Resume generation failed",
    });
  }
}

module.exports = {
  generateInterviewReportController,

  generateInterviewReportByIdController,

  getAllInterviewReportsController,

  generateResumePdfController,
};
